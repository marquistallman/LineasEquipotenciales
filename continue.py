# =========================
# GRAFICO EQUIPOTENCIAL
# =========================

x = df["x"]
y = df["y"]
z = df["V"]

triang = tri.Triangulation(x, y)

plt.figure(figsize=(10,7))

# mapa de color
contourf = plt.tricontourf(triang, z, levels=20)

# lineas equipotenciales
contour = plt.tricontour(triang, z,
                         levels=[2,5,10,15,18,19.78])

plt.clabel(contour, inline=True, fontsize=9)

# puntos medidos
plt.scatter(x, y, s=20)

plt.colorbar(contourf, label='Potencial (V)')

plt.title("Mapa de líneas equipotenciales")
plt.xlabel("x (0.1 cm)")
plt.ylabel("y (0.1 cm)")

plt.axis('equal')
plt.grid(True)

